var editorNginx = null;
var editorApache = null;
var currentInst = null;

function initAceEditor(elementId, mode) {
    var editor = ace.edit(elementId);
    editor.setTheme("ace/theme/twilight");
    editor.session.setMode("ace/mode/" + mode);
    editor.session.setOption("tabSize", 2);
    editor.setFontSize(14);
    editor.setOptions({ minLines: 20, maxLines: Infinity });
    editor.setReadOnly(true);
    editor.resize();
    return editor;
}

function getCfg() {
    return {
        ssl: $('#cfgSsl').prop('checked'),
        http2: $('#cfgHttp2').prop('checked'),
        port: $('#cfgPort').val() || '443',
        domain: $('#cfgDomain').val() || 'registry.example.com'
    };
}

function nginxConfig(inst, cfg) {
    var name = inst.name || 'default';
    var registryPort = inst.port || 5000;
    var distopsPort = '12680';
    var authType = inst.auth || 'token';
    var listenPort = cfg.port || '443';
    var domain = cfg.domain || 'registry.example.com';
    var ssl = cfg.ssl;
    var http2 = cfg.http2;

    var conf = '# Nginx reverse proxy for registry: ' + name + '\n';
    conf += '# Generated by distops\n\n';
    conf += '# Ensure Docker-Distribution-Api-Version header is always present\n';
    conf += 'map $upstream_http_docker_distribution_api_version $docker_distribution_api_version {\n';
    conf += "    '' 'registry/2.0';\n";
    conf += '}\n\n';
    conf += 'server {\n';

    if (ssl) {
        conf += '    listen ' + listenPort + ' ssl;\n';
        conf += '    listen [::]:' + listenPort + ' ssl;\n';
        conf += '    server_name ' + domain + ';\n\n';
        conf += '    ssl_certificate /etc/nginx/ssl/' + domain + '.pem;\n';
        conf += '    ssl_certificate_key /etc/nginx/ssl/' + domain + '-key.pem;\n';
        conf += '    ssl_protocols TLSv1.2 TLSv1.3;\n';
        conf += '    ssl_ciphers HIGH:!aNULL:!MD5;\n';
        if (http2) {
            conf += '    http2 on;\n';
        }
        conf += '\n';
    } else {
        conf += '    listen ' + listenPort + ';\n';
        conf += '    server_name ' + domain + ';\n\n';
    }

    conf += '    client_max_body_size 0;\n';
    conf += '    chunked_transfer_encoding on;\n\n';

    if (authType === 'token') {
        conf += '    # Auth endpoint - proxy to distops auth server\n';
        conf += '    location /auth/ {\n';
        conf += '        proxy_pass http://127.0.0.1:' + distopsPort + '/auth/' + name + ';\n';
        conf += '        proxy_set_header Host $host;\n';
        conf += '        proxy_set_header X-Real-IP $remote_addr;\n';
        conf += '        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;\n';
        conf += '        proxy_set_header X-Forwarded-Proto $scheme;\n';
        conf += '        proxy_pass_request_body off;\n';
        conf += '        proxy_set_header Content-Length "";\n';
        conf += '    }\n\n';
    }

    conf += '    # Registry v2 API\n';
    conf += '    location /v2/ {\n';
    conf += '        proxy_pass http://127.0.0.1:' + registryPort + ';\n';
    conf += '        proxy_set_header Host $http_host;\n';
    conf += '        proxy_set_header X-Real-IP $remote_addr;\n';
    conf += '        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;\n';
    conf += '        proxy_set_header X-Forwarded-Proto $scheme;\n';
    conf += '        add_header Docker-Distribution-Api-Version $docker_distribution_api_version always;\n';
    conf += '        proxy_read_timeout 900;\n';
    conf += '        proxy_send_timeout 900;\n';
    conf += '        proxy_buffering off;\n';
    conf += '        proxy_request_buffering off;\n';
    conf += '    }\n\n';
    conf += '    location / { return 200 "OK"; }\n';
    conf += '}\n';
    return conf;
}

function apacheConfig(inst, cfg) {
    var name = inst.name || 'default';
    var registryPort = inst.port || 5000;
    var distopsPort = '12680';
    var authType = inst.auth || 'token';
    var listenPort = cfg.port || '443';
    var domain = cfg.domain || 'registry.example.com';
    var ssl = cfg.ssl;

    var conf = '# Apache reverse proxy for registry: ' + name + '\n';
    conf += '# Generated by distops\n\n';
    conf += '<VirtualHost *:' + listenPort + '>\n';
    conf += '    ServerName ' + domain + '\n\n';

    if (ssl) {
        conf += '    SSLEngine on\n';
        conf += '    SSLCertificateFile /etc/ssl/certs/' + domain + '.pem\n';
        conf += '    SSLCertificateKeyFile /etc/ssl/private/' + domain + '-key.pem\n\n';
    }

    conf += '    LimitRequestBody 0\n\n';

    if (authType === 'token') {
        conf += '    # Auth endpoint - proxy to distops auth server\n';
        conf += '    <Location /auth/>\n';
        conf += '        ProxyPass http://127.0.0.1:' + distopsPort + '/auth/' + name + '\n';
        conf += '        ProxyPassReverse http://127.0.0.1:' + distopsPort + '/auth/' + name + '\n';
        conf += '    </Location>\n\n';
    }

    conf += '    # Registry v2 API\n';
    conf += '    <Location /v2/>\n';
    conf += '        ProxyPass http://127.0.0.1:' + registryPort + '\n';
    conf += '        ProxyPassReverse http://127.0.0.1:' + registryPort + '\n';
    conf += '        RequestHeader set X-Forwarded-Proto "' + (ssl ? 'https' : 'http') + '"\n';
    conf += '    </Location>\n\n';
    conf += '    ProxyTimeout 900\n';
    conf += '</VirtualHost>\n';
    return conf;
}

function generateConfigs() {
    if (!currentInst) return;
    var cfg = getCfg();
    if (editorNginx) editorNginx.setValue(nginxConfig(currentInst, cfg), -1);
    if (editorApache) editorApache.setValue(apacheConfig(currentInst, cfg), -1);
}

function copyText(text) {
    if (navigator.clipboard && navigator.clipboard.writeText) {
        navigator.clipboard.writeText(text).then(function() { layer.msg('Copied'); });
    } else {
        var ta = document.createElement('textarea');
        ta.value = text;
        ta.style.position = 'fixed';
        ta.style.left = '-9999px';
        document.body.appendChild(ta);
        ta.select();
        document.execCommand('copy');
        document.body.removeChild(ta);
        layer.msg('Copied');
    }
}


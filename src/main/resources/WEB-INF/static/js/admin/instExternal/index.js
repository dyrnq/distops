var editorNginx = null;
var editorApache = null;

function initAceEditor(elementId, mode) {
    var editor = ace.edit(elementId);
    editor.setTheme("ace/theme/twilight");
    editor.session.setMode("ace/mode/" + mode);
    editor.session.setOption("tabSize", 2);
    editor.setFontSize(14);
    editor.setOptions({ minLines: 20, maxLines: Infinity });
    editor.setReadOnly(true);
    editor.resize();
    return editor;
}

function nginxConfig(inst) {
    var name = inst.name || 'default';
    var port = inst.port || 5000;
    var distopsPort = '12680';
    var authType = inst.auth || 'token';

    var conf = '# Nginx reverse proxy for registry: ' + name + '\n';
    conf += '# Generated by distops\n\n';
    conf += 'server {\n';
    conf += '    listen 443 ssl http2;\n';
    conf += '    listen [::]:443 ssl http2;\n';
    conf += '    server_name registry.example.com;\n\n';
    conf += '    ssl_certificate /etc/nginx/ssl/registry.example.com.pem;\n';
    conf += '    ssl_certificate_key /etc/nginx/ssl/registry.example.com-key.pem;\n';
    conf += '    ssl_protocols TLSv1.2 TLSv1.3;\n';
    conf += '    ssl_ciphers HIGH:!aNULL:!MD5;\n\n';
    conf += '    client_max_body_size 0;\n';
    conf += '    chunked_transfer_encoding on;\n\n';

    if (authType === 'token') {
        conf += '    # Auth endpoint - proxy to distops auth server\n';
        conf += '    location /auth/ {\n';
        conf += '        proxy_pass http://127.0.0.1:' + distopsPort + '/auth/' + name + ';\n';
        conf += '        proxy_set_header Host $host;\n';
        conf += '        proxy_set_header X-Real-IP $remote_addr;\n';
        conf += '        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;\n';
        conf += '        proxy_set_header X-Forwarded-Proto $scheme;\n';
        conf += '        proxy_pass_request_body off;\n';
        conf += '        proxy_set_header Content-Length "";\n';
        conf += '    }\n\n';
    }

    conf += '    # Registry v2 API\n';
    conf += '    location /v2/ {\n';
    conf += '        proxy_pass http://127.0.0.1:' + port + ';\n';
    conf += '        proxy_set_header Host $http_host;\n';
    conf += '        proxy_set_header X-Real-IP $remote_addr;\n';
    conf += '        proxy_set_header X-Forwarded-For $proxy_add_x_forwarded_for;\n';
    conf += '        proxy_set_header X-Forwarded-Proto $scheme;\n';
    conf += '        proxy_read_timeout 900;\n';
    conf += '        proxy_send_timeout 900;\n';
    conf += '        proxy_buffering off;\n';
    conf += '        proxy_request_buffering off;\n';
    conf += '    }\n\n';
    conf += '    location / { return 200 "OK"; }\n';
    conf += '}\n';
    return conf;
}

function apacheConfig(inst) {
    var name = inst.name || 'default';
    var port = inst.port || 5000;
    var distopsPort = '12680';
    var authType = inst.auth || 'token';

    var conf = '# Apache reverse proxy for registry: ' + name + '\n';
    conf += '# Generated by distops\n\n';
    conf += '<VirtualHost *:443>\n';
    conf += '    ServerName registry.example.com\n\n';
    conf += '    SSLEngine on\n';
    conf += '    SSLCertificateFile /etc/ssl/certs/registry.example.com.pem\n';
    conf += '    SSLCertificateKeyFile /etc/ssl/private/registry.example.com-key.pem\n\n';
    conf += '    LimitRequestBody 0\n\n';

    if (authType === 'token') {
        conf += '    # Auth endpoint - proxy to distops auth server\n';
        conf += '    <Location /auth/>\n';
        conf += '        ProxyPass http://127.0.0.1:' + distopsPort + '/auth/' + name + '\n';
        conf += '        ProxyPassReverse http://127.0.0.1:' + distopsPort + '/auth/' + name + '\n';
        conf += '    </Location>\n\n';
    }

    conf += '    # Registry v2 API\n';
    conf += '    <Location /v2/>\n';
    conf += '        ProxyPass http://127.0.0.1:' + port + '\n';
    conf += '        ProxyPassReverse http://127.0.0.1:' + port + '\n';
    conf += '        RequestHeader set X-Forwarded-Proto "https"\n';
    conf += '    </Location>\n\n';
    conf += '    ProxyTimeout 900\n';
    conf += '</VirtualHost>\n';
    return conf;
}

function copyText(text) {
    if (navigator.clipboard && navigator.clipboard.writeText) {
        navigator.clipboard.writeText(text).then(function() { layer.msg('Copied'); });
    } else {
        var ta = document.createElement('textarea');
        ta.value = text;
        ta.style.position = 'fixed';
        ta.style.left = '-9999px';
        document.body.appendChild(ta);
        ta.select();
        document.execCommand('copy');
        document.body.removeChild(ta);
        layer.msg('Copied');
    }
}

